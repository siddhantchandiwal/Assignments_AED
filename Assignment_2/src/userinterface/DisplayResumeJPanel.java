    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblResume.getModel();
        dtm.setRowCount(0);

        for (Resume rs : rsh.getResumeHistory()) {
            Object row[] = new Object[3];
            row[0] = rs;
            row[1] = rs.getLastName();
            row[2] = rs.getEmailId();
            dtm.addRow(row);
        }
    }

