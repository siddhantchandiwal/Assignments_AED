    private void btnViewDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDetailsActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblResume.getSelectedRow();

        if (selectedRow >= 0) {
            Resume rs = (Resume) tblResume.getValueAt(selectedRow, 0);
            firstNameTextField.setText(rs.getFirstName());
            lastNameTextField.setText(rs.getLastName());
            emailIdTextField.setText(rs.getEmailId());
            contactNoTextField.setText(rs.getContactNo());
            streetLine1TextField.setText(rs.getStreetLn1());
            streetLine2TextField.setText(rs.getStreetLn2());
            cityTextField.setText(rs.getCity());
            zipCodeTextField.setText(rs.getZipCode());
            // countryTextField.setText(String.valueOf(rs.getCountry()));
            affiliationsTextField.setText(rs.getAffiliations());
            careerObjectiveTextField.setText(rs.getCareerObjective());
            degreeName1TextField.setText(rs.getDegreeName1());
            degreeDate1TextField.setText(rs.getDegreeDate1());
            gpa1TextField.setText(String.valueOf(rs.getGpa1()));
            degreeName2TextField.setText(rs.getDegreeName2());
            degreeDate2TextField.setText(rs.getDegreeDate2());
            gpa2TextField.setText(String.valueOf(rs.getGpa2()));
            ImageIcon icon = new ImageIcon(rs.getImage());
            picLabel.setIcon(icon);
            genderTextField.setText(rs.getGender());
            nationalityTextField.setText(rs.getNationality());
            graduatedTextField.setText(rs.getGraduated());
            languagesTextField.setText(rs.getProgrammingLang());
        } else {
            JOptionPane.showMessageDialog(this, "Please select any row", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnViewDetailsActionPerformed

    private void btnDeleteDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteDetailsActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblResume.getSelectedRow();

        if (selectedRow >= 0) {
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(this, "You want to delete selected Resume", "Confirm deletion", dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION) {
                Resume rs = (Resume) tblResume.getValueAt(selectedRow, 0);
                rsh.deleteResume(rs);
                JOptionPane.showMessageDialog(this, "Resume has been deleted", "Success", JOptionPane.INFORMATION_MESSAGE);
                populateTable();
                firstNameTextField.setText("");
                lastNameTextField.setText("");
                contactNoTextField.setText("");
                emailIdTextField.setText("");
                streetLine1TextField.setText("");
                streetLine2TextField.setText("");
                cityTextField.setText("");
                zipCodeTextField.setText("");
                countryTextField.setSelectedItem("US");
                affiliationsTextField.setText("");
                careerObjectiveTextField.setText("");
                degreeName1TextField.setText("");
                degreeDate1TextField.setText("");
                gpa1TextField.setText("");
                degreeName2TextField.setText("");
                degreeDate2TextField.setText("");
                gpa2TextField.setText("");
                workExperienceTextField.setSelectedItem("0-2 Years");
                languagesTextField.setText("");
                ImageIcon icon = new ImageIcon("");
                picLabel.setIcon(icon);
                genderTextField.setText("");
                nationalityTextField.setText("");
                graduatedTextField.setText("");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please select any row", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteDetailsActionPerformed

