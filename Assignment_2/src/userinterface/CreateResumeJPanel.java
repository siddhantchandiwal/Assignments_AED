    private String checkboxformat(){
        StringBuffer buffer=new StringBuffer();
        if(Java!=""){
            buffer=buffer.append(Java);
        }
        if(Python!=""){
            buffer.append(Python);
        }
        if(SQL!=""){
            buffer.append(SQL);
        }
        if(Cpp!=""){
            buffer.append(Cpp);
        }
        if(Php!=""){
            buffer.append(Php);
        }
        if(Matlab!=""){
            buffer.append(Matlab);
        }
        if(buffer.length()!=0){
        buffer=buffer.deleteCharAt(0);
        
        }
    return buffer.toString();}
    
    
