           r.setGpa1(gpa1);
           r.setDegreeName2(degreeName2);
           r.setDegreeDate2(degreeDate2);
           r.setGpa2(gpa2);
           r.setWorkExperience(workExperience);
           r.setImage(path);
           r.setGender(gen);
           r.setNationality(nat);
           r.setGraduated(grad);
           r.setProgrammingLang(checkboxformat());
