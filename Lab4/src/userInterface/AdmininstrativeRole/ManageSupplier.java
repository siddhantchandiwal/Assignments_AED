    private void btnViewSupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewSupplierActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblSupplierName.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select any row", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            Supplier s = (Supplier) tblSupplierName.getValueAt(selectedRow, 0);
            ViewSupplier vs = new ViewSupplier(userProcessContainer, s);
            userProcessContainer.add("ViewSupplier", vs);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnViewSupplierActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        int[] rows = tblSupplierName.getSelectedRows();

        if (rows.length > 0) {
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(this, "Would you like to delete Selected Rows ?", "Warning", dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION) {
                for (int k = rows.length - 1; k >= 0; k--) {
                    Supplier s = (Supplier) tblSupplierName.getValueAt(rows[k], 0);
                    supplierDirectory.removeSupplier(s);
                    populateTable();
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_btnRemoveActionPerformed

